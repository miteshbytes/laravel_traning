    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'email' => 'required|email',
            'password' => 'required'
        ]);

        $user = User::where('email', $request['email'])
        //->where('role_id', 1)
        ->first();

        if($user)
        {
            $validCredentials = Hash::check($request['password'], $user->password);
            if ($validCredentials) {
                // return $user;
                $request->session()->put('user_data', $user->toArray());
                return redirect()->route('roles.index')->with('success', 'Thank you login sucessfully.');
            }
            else
            {
                return redirect()->route('login.index')->with('error', 'Password is invalid!');
            }
        }
        else
        {
            return redirect()->route('login.index')->with('error', 'Email address is invalid!');
        }

    }

